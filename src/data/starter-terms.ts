export function starterTermToDTO(term: StarterTerm): TermDTO {
  const mappedCategories = term.categorySlugs
    .map((slug) => categories.find((category) => category.slug === slug))
    .filter(Boolean) as CategoryDTO[];

  return {
    term: term.term,
    slug: term.slug,
    shortDefinition: term.shortDefinition,
    definition: term.definition,
    examples: term.examples,
    categories: mappedCategories,
    categorySlugs: term.categorySlugs,
    status: term.status,
    relatedTerms: (term.relatedSlugs ?? [])
      .map((slug) => starterTerms.find((related) => related.slug === slug))
      .filter(Boolean)
      .map((related) => ({
        term: related!.term,
        slug: related!.slug,
        shortDefinition: related!.shortDefinition,
        status: related!.status,
      })),
  };
}

export const starterTermDTOs = starterTerms.map(starterTermToDTO);
